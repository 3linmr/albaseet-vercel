# إدارة العملاء

## إضافة عميل جديد
لإضافة عميل جديد في نظام witsUP:

### 1. الدخول إلى شاشة العملاء
- من القائمة الرئيسية، اختر "المبيعات".
- ثم اختر "العملاء".

### 2. إضافة عميل جديد
- اضغط على زر "إضافة عميل جديد" (+).
- املأ البيانات المطلوبة:
  - **الاسم**: اسم العميل (إلزامي).
  - **رقم الهاتف**: رقم هاتف العميل.
  - **البريد الإلكتروني**: بريد العميل الإلكتروني.
  - **العنوان**: عنوان العميل.
  - **نوع العميل**: فردي أو شركة.
  - **رقم الهوية**: رقم هوية العميل أو السجل التجاري.

### 3. حفظ العميل
- اضغط على زر "حفظ" لحفظ بيانات العميل.
- ستظهر رسالة تأكيد بنجاح الإضافة.

## تعديل بيانات العميل
لتعديل بيانات عميل موجود:

### 1. البحث عن العميل
- استخدم شريط البحث للعثور على العميل.
- أو تصفح قائمة العملاء.

### 2. تعديل البيانات
- انقر على العميل لفتح بياناته.
- اضغط على زر "تعديل".
- قم بتعديل البيانات المطلوبة.
- اضغط على "حفظ" لحفظ التعديلات.

## حذف العميل
لحذف عميل:

### 1. اختيار العميل
- اختر العميل من القائمة.
- اضغط على زر "حذف".

### 2. تأكيد الحذف
- ستظهر رسالة تأكيد.
- اضغط على "تأكيد" لحذف العميل نهائياً.

**ملاحظة**: لا يمكن حذف العميل إذا كان لديه فواتير أو معاملات مرتبطة به.

