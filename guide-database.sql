# إدارة الفواتير

## إنشاء فاتورة بيع
لإنشاء فاتورة بيع في نظام witsUP:

### 1. الدخول إلى شاشة الفواتير
- من القائمة الرئيسية، اختر "المبيعات".
- ثم اختر "دورات البيع".
- ثم اختر "دورة البيع المباشر".
- ثم اختر "فاتورة بيع نقداً" أو "فاتورة بيع آجل".

### 2. إضافة بيانات الفاتورة
- **العميل**: اختر العميل من القائمة.
- **التاريخ**: تاريخ الفاتورة.
- **طريقة الدفع**: نقدي أو آجل.
- **الفرع**: الفرع المسؤول عن الفاتورة.

### 3. إضافة المنتجات
- اضغط على "إضافة منتج".
- اختر المنتج من القائمة.
- أدخل الكمية والسعر.
- اضغط على "إضافة" لإضافة المنتج للفاتورة.

### 4. حفظ الفاتورة
- اضغط على زر "حفظ" لحفظ الفاتورة.
- أو اضغط على "حفظ وإرسال" لإرسال الفاتورة للعميل.

## تعديل الفاتورة
لتعديل فاتورة موجودة:

### 1. البحث عن الفاتورة
- استخدم شريط البحث للعثور على الفاتورة.
- أو تصفح قائمة الفواتير.

### 2. تعديل الفاتورة
- انقر على الفاتورة لفتحها.
- اضغط على زر "تعديل".
- قم بالتعديلات المطلوبة.
- اضغط على "حفظ" لحفظ التعديلات.

**ملاحظة**: لا يمكن تعديل الفاتورة بعد اعتمادها أو إرسالها للعميل.

