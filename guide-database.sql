لإضافة منتج جديد في نظام witsUP:

### 1. الدخول إلى شاشة المنتجات
- من القائمة الرئيسية، اختر "المخازن".
- ثم اختر "المنتجات".

### 2. إضافة منتج جديد
- اضغط على زر "إضافة منتج جديد" (+).
- املأ البيانات المطلوبة:
  - **اسم المنتج**: اسم المنتج (إلزامي).
  - **كود المنتج**: كود المنتج الفريد.
  - **الوصف**: وصف المنتج.
  - **الفئة**: فئة المنتج.
  - **السعر**: سعر المنتج.
  - **وحدة القياس**: وحدة قياس المنتج (قطعة، كيلو، متر...).
  - **الحد الأدنى**: الحد الأدنى للمخزون.

### 3. حفظ المنتج
- اضغط على زر "حفظ" لحفظ بيانات المنتج.
- ستظهر رسالة تأكيد بنجاح الإضافة.

## تعديل بيانات المنتج
لتعديل بيانات منتج موجود:

### 1. البحث عن المنتج
- استخدم شريط البحث للعثور على المنتج.
- أو تصفح قائمة المنتجات.

### 2. تعديل البيانات
- انقر على المنتج لفتح بياناته.
- اضغط على زر "تعديل".
- قم بتعديل البيانات المطلوبة.
- اضغط على "حفظ" لحفظ التعديلات.

## حذف المنتج
لحذف منتج:

### 1. اختيار المنتج
- اختر المنتج من القائمة.
- اضغط على زر "حذف".

### 2. تأكيد الحذف
- ستظهر رسالة تأكيد.
- اضغط على "تأكيد" لحذف المنتج نهائياً.

**ملاحظة**: لا يمكن حذف المنتج إذا كان لديه حركة مخزنية أو فواتير مرتبطة به.

